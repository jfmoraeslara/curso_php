<?php

    // tipo um if e else simplificado
    echo 20 > 10 ? "Deu true <br>" : "Deu false <br>"; //true

    echo 20 > 50 ? "Deu true <br>" : "Deu false <br><br>"; //false

    $a = 10;
    $b = 5;

    echo $a > $b ? "Deu true <br>" : "Deu false <br>"; //true

    echo $a === $b ? "Deu true <br>" : "Deu false <br><br>"; //false

    echo $a === $b || 10 > 5 ? "Deu true <br>" : "Deu false <br>"; //true

    echo $a === $b && 10 > 5 ? "Deu true <br>" : "Deu false <br>"; //false
?>